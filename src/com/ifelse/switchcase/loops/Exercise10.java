package com.ifelse.switchcase.loops;

public class Exercise10 {

	public static void main(String[] args) {

		 for (int i = 1; i <= 5; i++) {
			 System.out.println("I am Batman");
		 }

		 int i = 1;
		 while (i <= 5) {
			 System.out.println("I am Batman");
			 i++;
		 }


		int j = 1;
		do {
			j++;
			System.out.println("I am Batman");
		} while (j <= 5);

	}

}
