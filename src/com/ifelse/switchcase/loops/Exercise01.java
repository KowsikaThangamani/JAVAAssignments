package com.ifelse.switchcase.loops;

public class Exercise01 {

	public static void main(String[] args) {

		int a = 25, b = 78, c = 87;

		if (a>b && a>c) {
			System.out.println("a is greater");
		}
		else if (b>c) {
			System.out.println("b is greater");
		}
		else {
			System.out.println("c is greater");
		}

	}

}
